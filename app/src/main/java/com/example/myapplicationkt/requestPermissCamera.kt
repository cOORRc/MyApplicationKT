class requestPermissCamera: AppCompatActivity() {
    companion object {
        private const val CAMERA_PERMISSION_CODE = 100
        //private const val STORAGE_PERMISSION_CODE = 101
    }

/*    override fun onCreate(savedInstanceState: Bundle?) {
